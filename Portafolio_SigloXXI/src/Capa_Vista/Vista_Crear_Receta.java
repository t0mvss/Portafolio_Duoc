    public void LlenarCMBPlato(){
        try {
            cmbPlato.removeAllItems();
            ListaPlato = controlador.getListaPlato();
            Iterator iter = ListaPlato.iterator();
            while (iter.hasNext()){
                Plato plato= (Plato) iter.next();
                cmbPlato.addItem(plato.toString());
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this,"Hubo un error","Aviso",JOptionPane.WARNING_MESSAGE);
        }
        
    }
    

