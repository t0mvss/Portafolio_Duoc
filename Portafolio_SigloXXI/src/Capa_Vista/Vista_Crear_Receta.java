    private void btnCrearRecetaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearRecetaActionPerformed
        String nomReceta = txtNombreReceta.getText();
        if (nomReceta.equals("")){
            JOptionPane.showMessageDialog(this, "La receta debe tener un nombre","Error",JOptionPane.ERROR_MESSAGE);
        }
        else{
        String nombrePlato;
        String nombreReceta;
        int id = 0;
        PreparedStatement ps = null;
        ResultSet rs = null;
        String sql = "";
        int i = JOptionPane.showConfirmDialog(this,"¿Está seguro de que desea agregar esta Receta?","Mensajes", JOptionPane.ERROR_MESSAGE);
        if (i == 0){
            try {
                nombrePlato = cmbPlato.getSelectedItem().toString();
                nombreReceta = txtNombreReceta.getText();
                sql = "select id from plato Where name = ?;";
                ps = cc.prepareStatement(sql);
                ps.setString(1, nombrePlato);
                rs = ps.executeQuery();
               if (rs.next()){
                    id = Integer.parseInt(rs.getString("id"));
                }
                CallableStatement insert = cc.prepareCall("{call insertarReceta(?,?)}"); 
                insert.setInt(1, id);
                insert.setString(2, nombreReceta);
                insert.execute();
                JOptionPane.showMessageDialog(this,"¡Receta agregada exitosamente!","Mensajes", JOptionPane.INFORMATION_MESSAGE);
                cmbPlato.setSelectedIndex(0);
            } 
            catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Problemas de conexión con la Base de Datos","Mensajes", JOptionPane.ERROR_MESSAGE);
            }
        }
        }
    }//GEN-LAST:event_btnCrearRecetaActionPerformed

