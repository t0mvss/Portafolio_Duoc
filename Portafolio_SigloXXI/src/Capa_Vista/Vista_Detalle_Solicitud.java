    public void mostrarTablaDetalle(){
        int idsoli;
        Vista_Solicitudes visSoli = new Vista_Solicitudes();
        idsoli = Integer.parseInt(visSoli.id_soli);
        PreparedStatement ps = null;
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("ID DETALLE");
        modelo.addColumn("NOMBRE");
        modelo.addColumn("CANTIDAD");
        modelo.addColumn("MEDIDA");
        tblDetalle.setModel(modelo);
        String sql = "";
        sql = "select de.id_detalle, i.nombre, de.cantidad, tp.descripcion from detalle_solicitud de inner join insumos i on i.id_insumos = de.id_insumo inner join tipo_medida tp on tp.id_tipo_medida = i.id_tipo_medida where de.id_solicitud_insumo = ? order by de.id_detalle asc;";
        String [] dato = new String[4];
        try{
            ps = cc.prepareStatement(sql);
            ps.setInt(1, idsoli);
            ResultSet rs = ps.executeQuery();
            while(rs.next()){
                dato[0] = rs.getString(1);
                dato[1] = rs.getString(2);
                dato[2] = rs.getString(3);
                dato[3] = rs.getString(4);
                modelo.addRow(dato);
            }
            tblDetalle.setModel(modelo);
        }
        catch(SQLException e){
        }

    }
    

